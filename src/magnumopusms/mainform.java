    private void buttonWorkersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonWorkersActionPerformed
        PreparedStatement ps;
        ResultSet rs;
        String tipo = ("admin");
        
        String query = "SELECT * FROM login  WHERE id = ? AND tipo_user = ?";
        try{
            ps = dbConnection.getConnection().prepareStatement(query);
            
            ps.setInt(1, idUser);
            ps.setString(2, tipo);
            
            rs = ps.executeQuery();
        if (rs.next()){
            funcionarios funcionarios = new funcionarios();
            funcionarios.setVisible(true);
            funcionarios.pack();
            funcionarios.setLocationRelativeTo(null);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Apenas administradores podem aceder a esta área!", "Falta de permissões", 2);
        }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_buttonWorkersActionPerformed

