    private void buttonUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUserActionPerformed
        PreparedStatement ps;
        ResultSet rs;
        String query= "SELECT * FROM login WHERE id = '"+idUser+"'";
        try{
            ps = dbConnection.getConnection().prepareStatement(query);
            rs = ps.executeQuery();
            while (rs.next()){
                //MessageDigest m=MessageDigest.getInstance("MD5");
                //m.update(s.getBytes(),0,s.length());
                utilizador utilizador = new utilizador();
                utilizador.setVisible(true);
                utilizador.pack();
                utilizador.setLocationRelativeTo(null);
                utilizador.fieldNomeFunc.setText(rs.getString("nome_func"));
                utilizador.fieldUsername.setText(rs.getString("username"));
                utilizador.fieldPassword.setText(rs.getString("password"));
                this.dispose();
            }
        }catch(Exception ex){}
    }//GEN-LAST:event_buttonUserActionPerformed

