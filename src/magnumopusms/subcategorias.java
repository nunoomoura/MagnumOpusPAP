    public void executeSQLQuery(String query, String message){
        Connection con = dbConnection.getConnection();
        Statement st;
        try{
            st = con.createStatement();
            if(st.executeUpdate(query) == 1){
                JOptionPane.showMessageDialog(null, "Dados "+message+" com sucesso");
            }else{
                JOptionPane.showMessageDialog(null, "Dados n√£o "+message);
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    public ArrayList<SubCat> subcatList(){
        ArrayList<SubCat> subcatList = new ArrayList<>();
        PreparedStatement ps;
        ResultSet rs;
        String query= "SELECT * FROM sub_categorias WHERE cod_categoria = '"+idCat+"'";
        
        try{
            ps = dbConnection.getConnection().prepareStatement(query);
            rs = ps.executeQuery();
            SubCat subcat;
            while(rs.next()){
                subcat = new SubCat(rs.getInt("cod_subcategoria"), rs.getInt("cod_categoria"), rs.getString("nome_subcategoria"), rs.getFloat("iva"));
                subcatList.add(subcat);
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        return subcatList;
    }
    public void show_subcat(){
        ArrayList<SubCat> list = subcatList();
        DefaultTableModel model = (DefaultTableModel)tableSubCats.getModel();
        Object[] row = new Object[3];
        for(int i=0; i<list.size();i++){
            row[0]=list.get(i).getId();
            row[1]=list.get(i).getNome_subcat();
            row[2]=list.get(i).getIVA();
           
            model.addRow(row);
        }
    }

